'''Assign a message to a variable, and print that message.
Then change the value of the variable to a new message, and print the new
message.'''
# assign a value to a variable (nh)
nh = "this is my code"
# print the variable 
print (nh)
# when this is run it will print (this is my code) 
# now assign the same variable (nh) another value 
nh = "this is my new code"
# now if we print this variable it will first print (this is my code)
# then it will print (this is my new code )
print (nh)
